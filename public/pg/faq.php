    <p class="pregunta">10.- ¿Qué debo hacer si pierdo un libro que tengo en préstamo?</p>
    <div class="respuesta">
        <p class="respuesta_texto">En caso de pérdida del material facilitado, el usuario deberá proceder de la siguiente manera:</p>
        <ul class="ygul">
        <li>Comunicar la perdida inmediatamente a la Dirección de Biblioteca.</li>
        <li>Deberá reponer el material. Éste debe ser nuevo y tiene que venir acompañado de la boleta respectiva (no se aceptan fotocopias).</li>
        <li>El usuario tendrá 30 días para reponer el material y quedar exento de pagar la multa, pasado este plazo, deberá reponer el material y cancelar la deuda. En caso de no encontrarse el título en el comercio, deberá entregar dentro del mismo plazo (30 días), material equivalente a la temática según decida la Dirección de Biblioteca.</li>
        <li>El usuario no podrá hacer uso de los servicios de las bibliotecas, ni realizar trámites en que se requiera la firma de Biblioteca (retiro temporal, renuncia a la carrera, solicitar certificado de no deuda para la obtención del título profesional), hasta que se reponga el material  extraviado y cancele la multa correspondiente. </li> 
    </div>

    <p class="pregunta">11.- ¿En qué instancias puedo recurrir a la condonación y/o conmutación de mi multa monetaria?</p>
    <div class="respuesta">
        <p class="respuesta_texto">La condonación total de la multa al usuario, se realizará de acuerdo a las razones expuestas a continuación:</p>
        <ul class="ygul">
            <li><strong>Biblioteca cerrada</strong>: En caso que el servicio se encuentre cerrado en los horarios que presta atención, ya sea por cambio de actividades, reuniones u otras actividades o eventos.</li>
            <li><strong>Desastres naturales</strong>: En caso de producirse algún fenómeno de la naturaleza que impida al usuario acercarse a devolver el material.</li>
            <li><strong>Enfermedad</strong>: Aquellos usuarios que presenten algún problema de salud durante el préstamo del material, que éste le imposibilite la devolución oportuna, deberán acreditar presentando su certificado médico (visado por SEMDA).</li>
            <li><strong>Error del funcionario</strong>: En los casos que el funcionario cometa un error, el encargado de circulación deberá condonar la multa.</li>
            <li><strong>Vigencia usuario</strong>: Se condonará la multa de aquellos usuarios que no hayan podido renovar por problemas de vigencia.</li>
            <li><strong>Caída del sistema</strong>: Se condonará la multa generada por caída o error del sistema, cuando se presentan cortes de energía, ventanas de mantenimiento, caídas del servicio de internet, actualización de nuevas versiones del sistema de información.</li>
        </ul>

        <p class="respuesta_texto">Condonación parcial y conmutación de la deuda</p>
        <p class="respuesta_texto">En el caso de aquellas multas que excedan los $20.000, el usuario podrá acogerse a la condonación de un 50% de la multa. El saldo pendiente lo podrá conmutar con la donación de libros que enriquezcan el fondo bibliográfico de su carrera respectiva, los cuales deberán ser nuevos y respaldado con su boleta.</p>
    </div>

